CREATE TABLE PHOTOS (
ID INT NOT NULL AUTO_INCREMENT,
NAME VARCHAR(45),
WEIGHT DOUBLE,
LENGTH DOUBLE,
LATITUDE VARCHAR(45),
LONGITUDE VARCHAR(45),
USERNAME VARCHAR(45),
UPDATED_AT TIMESTAMP NOT NULL DEFAULT NOW() ON UPDATE NOW(),
CREATED_AT TIMESTAMP NOT NULL DEFAULT NOW(),
PRIMARY KEY (ID));

